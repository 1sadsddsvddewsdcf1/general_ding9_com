package com.ding9.multipleentity;

public class MucommendInfo {
  private String name;
  private int id;
  private String time;
  private int count;
  private int countture;
  private int level;
  private String comtend;
  
  public String getComtend() { return this.comtend; }
  
  public void setComtend(String comtend) {
    this.comtend = comtend;
  }
  
  public int getCount() { return this.count; }
  
  public void setCount(int count) {
    this.count = count;
  }
  
  public int getCountture() { return this.countture; }
  
  public void setCountture(int countture) {
    this.countture = countture;
  }
  
  public String getName() { return this.name; }
  
  public void setName(String name) {
    this.name = name;
  }
  
  public String getTime() { return this.time; }
  
  public void setTime(String time) {
    this.time = time;
  }
  
  public int getLevel() { return this.level; }
  
  public void setLevel(int level) {
    this.level = level;
  }
  
  public int getId() { return this.id; }
  
  public void setId(int id) {
    this.id = id;
  }
}
